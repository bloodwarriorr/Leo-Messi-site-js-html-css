class Product {

    id
    name
    Price
    SalePrice
    image
    Description
    Category
    Amount
    TotalPrice

    constructor(id, name, price, salePrice, image, description, category,amount,Totalprice) {
        this.id = id
        this.name = name
        this.Price = price
        this.SalePrice = salePrice
        this.image = image
        this.Description = description
        this.Category = category
        this.Amount=amount
        this.TotalPrice=Totalprice
    }




}